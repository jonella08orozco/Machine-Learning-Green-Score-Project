#API call to OpenMeteo for the ten cities in globalgreen, including its current features
#plus the new features we added for uniqueness.

api = "https://archive-api.open-meteo.com/v1/archive?latitude=40.71,35.68,51.5,19.07,-33.86,52.52,-23.55,30.04,6.52,39.9&longitude=-74,139.76,-0.12,72.87,151.2,13.4,-46.63,31.23,3.37,116.4&start_date=2025-01-01&end_date=2025-12-31&hourly=temperature_2m,relative_humidity_2m,cloud_cover,wind_speed_100m,shortwave_radiation"

#read the data which is in JSON formatting
data = pd.read_json(api)

data.head()

#parse data from JSON to pandas DataFrame
all_hourly = []

for index, row in data.iterrows():
    loc_hourly_data = pd.DataFrame(row["hourly"])
    loc_hourly_data["latitude"] = row["latitude"]
    loc_hourly_data["longitude"] = row["longitude"]
    all_hourly.append(loc_hourly_data)

df = pd.concat(all_hourly, ignore_index = True)

df.head()

#latitude and longitude differ in both df and unique_lat_long, so we need to
#ensure formats are the same. we chose integer type as the exact coordinate
#locations are not necessary for the data analysis we will be performing

