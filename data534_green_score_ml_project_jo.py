#moving on to recreating the green score feature, which will involve using the
#minimum and maximums for both shortwave radiation and windspeed, stored in these
#local variables.

solar_max = df.shortwave_radiation.max()
solar_min = df.shortwave_radiation.min()

wind_max = df.wind_speed_100m.max()
wind_min = df.wind_speed_100m.min()

"""Important note:

"green_score" is not an official sustainability metric used by any organization. It’s a custom, engineered index created specifically for this dataset to make analysis easier.

In simple terms, it combines solar radiation and wind speed to represent how favorable the conditions are for renewable energy generation at a given hour.

It is a derived feature designed for comparative analysis and machine learning, rather than a real-world certification or rating.
"""

df.loc[:, "green_score"] = ((0.5 * ((df["shortwave_radiation"] - solar_min) / (solar_max - solar_min)))
+ (0.5 * ((df["wind_speed_100m"] - wind_min) / (wind_max - wind_min))))

#normalized_value = (x − min) / (max − min)
#green_score = 0.5 * solar_normalized + 0.5 * wind_normalized
#(the equation we received from Nudrat, upon request, in order to recreate her dataset)

