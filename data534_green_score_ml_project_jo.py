df["longitude"] = df["longitude"].astype(int)

unique_lat_lon["latitude"] = unique_lat_lon["latitude"].astype(int)
unique_lat_lon["longitude"] = unique_lat_lon["longitude"].astype(int)

df = pd.merge(df, unique_lat_lon, on = ["latitude", "longitude"], how = "left")

